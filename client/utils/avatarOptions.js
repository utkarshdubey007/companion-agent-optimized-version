// Local storage key
export const AVATAR_CONFIG_KEY = 'avatarCreatorConfig';

// Utility functions
export const saveAvatarConfig = (config) => {
  localStorage.setItem(AVATAR_CONFIG_KEY, JSON.stringify(config));
};

export const loadAvatarConfig = () => {
  try {
    const saved = localStorage.getItem(AVATAR_CONFIG_KEY);
    return saved ? JSON.parse(saved) : defaultAvatarConfig;
  } catch (error) {
    console.error('Error loading avatar config:', error);
    return defaultAvatarConfig;
  }
};

export const resetAvatarConfig = () => {
  localStorage.removeItem(AVATAR_CONFIG_KEY);
  return { ...defaultAvatarConfig };
};
